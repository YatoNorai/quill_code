    signatureText:            Color(0xFFE0E0FF),
    signatureHighlightedParam:Color(0xFF00FFCC),
    textActionBackground:     Color(0xFF1E1E30),
    textActionIconColor:      Color(0xFFE0E0FF),
  );
}
